          <h3>Squat</h3>
          <p>A squat is a strength exercise in which the trainee lowers their hips from a standing position and then stands back up. During the descent of a squat, the hip and knee joints flex while the ankle joint dorsiflexes; conversely the hip and knee joints extend and the ankle joint plantarflexes when standing up. </p>
         </div>
         <div class="box-power w33 left">
          <img class="BenchPress"src="/projeto_php/img/2021_02_22_06_55_49_1_Julius_maddox_Powerlifter_Facebook_Mozilla_Firefox.jpg" alt="">
          <h3>Bench Press</h3>
          <p>The bench press, or chest press, is an upper-body weight training exercise in which the trainee presses a weight upwards while lying on a weight training bench. The exercise uses the pectoralis major, the anterior deltoids, and the triceps, among other stabilizing muscles. A barbell is generally used to hold the weight, but a pair of dumbbells can also be used.</p>
         </div>
         <div class="box-power w33 left">
          <img class="Deadlift"src="/projeto_php/img/download.jpg" alt="">
          <h3>Deadlift</h3>
          <p>Deadlift refers to the lifting of dead weight (weight without momentum), such as weights lying on the ground. It is one of the few standard weight training exercises in which all repetitions begin with dead weight. In most other lifts, there is an eccentric (lowering of the weight) phase followed by the concentric (lifting of the weight) phase. During these exercises, a small amount of energy is stored in the stretched muscles and tendons in the eccentric phase if the lifter is not flexible beyond the range of motion.</p>
