         <h3> Snatch</h3>
         <p>The snatch is the first of two lifts contested in the sport of weightlifting (also known as Olympic weightlifting) followed by the clean and jerk. The objective of the snatch is to lift the barbell from the ground to overhead in one continuous motion. There are four main styles of snatch used: squat snatch (or full snatch), split snatch, power snatch, and muscle snatch. The squat snatch and split snatch are the most common styles used in competition while power snatch and muscle snatch are mostly used for training purposes.</p>
        </div>
